let number = 1;

switch (number) {
    case 1:
        console.log('El número es 1!');
        break;
    case 10:
        console.log('El número es 10!');
        break;
    case 100:
        console.log('El número es 100!');
        break;
    default:
        console.log('Número no identificado');
}